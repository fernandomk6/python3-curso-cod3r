# int
print(type(1)) 
# float
print(type(1.1)) 
# str
print(type('texto'))
# bool
print(type(True))
# bool
print(type(False))
# Listas (list): São coleções ordenadas e mutáveis de elementos.
print(type([1, 2, 3]))
# Tuplas (tuple): São coleções ordenadas e imutáveis de elementos.
print(type((1, 2, 3)))
# Conjuntos (set): São coleções não ordenadas de elementos únicos.
print(type({1, 2, 3, 4, 5}))
# Dicionários (dict): São coleções de pares chave-valor, onde cada valor é acessado por uma chave única.
print(type({"chave1": "valor1", "chave2": "valor2"}))
